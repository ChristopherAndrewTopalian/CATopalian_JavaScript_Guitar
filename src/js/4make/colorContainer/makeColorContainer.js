function makeColorContainer()
{
    let colorContainer = ce('div');
    colorContainer.id = 'colorContainer';
    colorContainer.style.position = 'absolute';
    colorContainer.style.left = '200px';
    colorContainer.style.bottom = '-15px';
    ba(colorContainer);
}

