function makeMiddleContainer()
{
    let middleContainer = ce('div');
    middleContainer.id = 'middleContainer';
    middleContainer.style.position = 'absolute';
    middleContainer.style.left = 160 + 'px';
    middleContainer.style.top = 25 + 'px';
    middleContainer.style.display = 'flex';
    middleContainer.style.flexDirection = 'column';
    ba(middleContainer);
}

