function makePlayContainer()
{
    let playContainer = ce('div');
    playContainer.id = 'playContainer';
    playContainer.style.position = 'absolute';
    playContainer.style.left = '200px';
    playContainer.style.bottom = '10px';
    ba(playContainer);
}

