function makeRecordPlayStatus()
{
    let mode = ce('div');
    mode.id = 'mode';
    mode.textContent = 'Record Mode';
    mode.style.position = 'absolute';
    mode.style.left = 300 + 'px';
    mode.style.bottom = 30 + 'px';
    ba(mode);
}

